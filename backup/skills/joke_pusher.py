# 已发送笑话记录文件
SENT_FILE = os.path.join(os.path.dirname(__file__), "sent_jokes.json")

def load_sent_jokes():
    """加载已发送的笑话索引"""
    if os.path.exists(SENT_FILE):
        with open(SENT_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    return []

def save_sent_jokes(sent_indices):
    """保存已发送的笑话索引"""
    with open(SENT_FILE, "w", encoding="utf-8") as f:
        json.dump(sent_indices, f, ensure_ascii=False)

def get_random_joke():
    """随机获取一个未重复的笑话"""
    sent_indices = load_sent_jokes()
    available_indices = [i for i in range(len(JOKES)) if i not in sent_indices]
    
    # 如果所有笑话都发过了，重置
    if not available_indices:
        sent_indices = []
        available_indices = list(range(len(JOKES)))
        reset_msg = "🔄 笑话库已重置，开始新一轮！"
    else:
        reset_msg = None
    
    # 随机选择一个
    chosen_index = random.choice(available_indices)
    sent_indices.append(chosen_index)
    save_sent_jokes(sent_indices)
    
    return JOKES[chosen_index], len(available_indices) - 1, reset_msg

def format_message(joke, remaining, reset_msg=None):
    """格式化消息"""
    now = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    content = f"""🦞 龙虾养殖场 · 每日笑话

📅 时间：{now}
📊 剩余不重复笑话：{remaining} 个

{joke}

😄 开心每一天！
"""
    if reset_msg:
        content = reset_msg + "\n\n" + content
    return content

if __name__ == "__main__":
    joke, remaining, reset_msg = get_random_joke()
    message = format_message(joke, remaining, reset_msg)
    print(message)
